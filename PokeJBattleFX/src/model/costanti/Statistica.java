package model.costanti;

/**
 * Enum delle caratteristiche specifiche del pokemon.
 * 
 * @author Simone Comignani, Simone Descontus
 * @version 1.0
 */
public enum Statistica {
	/**
     * quantifica il danno fisico del pokemon
     */
	ATTACCO,
	/**
     * quantifica il danno speciale del pokemon
     */
	ATTACCO_SPECIALE,
	/**
     * quantifica la difesa fisica del pokemon
     */
	DIFESA,
	/**
     * quantifica la difesa speciale del pokemon
     */
	DIFESA_SPECIALE,
	/**
     * quantifica la velocita del pokemon
     */
	VELOCITA,
	
	/**
     * quantifica la precisione del pokemon
     */
	PRECISIONE,
	/**
     * quantifica l'elusione del pokemon
     */
	ELUSIONE;
}
